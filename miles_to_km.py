# [Input]
# Get Values for miles
Miles = int(input("Number of miles?: "))
#Processing
#Calculate km by the formula km = m * 1.60934
Km = Miles * 1.60934

#[Output]
#Display calculated km
print(Miles, " miles is equivalent to ", Km, " Kilometers")
